package cat3.connection.vo;

public class Space_ImgVO {
	private int space_img_no;
	private String space_img_path;
	private int space_info_no;
	
	
	public Space_ImgVO() {
		super();
	}
	public Space_ImgVO(int space_img_no, String space_img_path, int space_info_no) {
		super();
		this.space_img_no = space_img_no;
		this.space_img_path = space_img_path;
		this.space_info_no = space_info_no;
	}
	public int getSpace_img_no() {
		return space_img_no;
	}
	public void setSpace_img_no(int space_img_no) {
		this.space_img_no = space_img_no;
	}
	public String getSpace_img_path() {
		return space_img_path;
	}
	public void setSpace_img_path(String space_img_path) {
		this.space_img_path = space_img_path;
	}
	public int getSpace_info_no() {
		return space_info_no;
	}
	public void setSpace_info_no(int space_info_no) {
		this.space_info_no = space_info_no;
	}

		
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
