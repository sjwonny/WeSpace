package cat3.connection.vo;

public class Space_TagVO {
	private int space_tag_no;
	private String space_tag_name;
	private int space_info_no;
	
	
	public Space_TagVO() {
		super();
	}
	public Space_TagVO(int space_tag_no, String space_tag_name, int space_info_no) {
		super();
		this.space_tag_no = space_tag_no;
		this.space_tag_name = space_tag_name;
		this.space_info_no = space_info_no;
	}
	public int getSpace_tag_no() {
		return space_tag_no;
	}
	public void setSpace_tag_no(int space_tag_no) {
		this.space_tag_no = space_tag_no;
	}
	public String getSpace_tag_name() {
		return space_tag_name;
	}
	public void setSpace_tag_name(String space_tag_name) {
		this.space_tag_name = space_tag_name;
	}
	public int getSpace_info_no() {
		return space_info_no;
	}
	public void setSpace_info_no(int space_info_no) {
		this.space_info_no = space_info_no;
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
