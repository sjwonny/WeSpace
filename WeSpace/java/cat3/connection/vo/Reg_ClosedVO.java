package cat3.connection.vo;

public class Reg_ClosedVO {
	private int reg_closed_no;
	private int day_week_no;
	private int usage_info_no;
	
	
	
	
	public Reg_ClosedVO() {
		super();
	}




	public Reg_ClosedVO(int reg_closed_no, int day_week_no, int usage_info_no) {
		super();
		this.reg_closed_no = reg_closed_no;
		this.day_week_no = day_week_no;
		this.usage_info_no = usage_info_no;
	}




	public int getReg_closed_no() {
		return reg_closed_no;
	}




	public void setReg_closed_no(int reg_closed_no) {
		this.reg_closed_no = reg_closed_no;
	}




	public int getDay_week_no() {
		return day_week_no;
	}




	public void setDay_week_no(int day_week_no) {
		this.day_week_no = day_week_no;
	}




	public int getUsage_info_no() {
		return usage_info_no;
	}




	public void setUsage_info_no(int usage_info_no) {
		this.usage_info_no = usage_info_no;
	}
	
	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
