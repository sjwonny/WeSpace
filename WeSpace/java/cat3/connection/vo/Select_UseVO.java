package cat3.connection.vo;

public class Select_UseVO {
	private int select_use_no;
	private int space_uses_no;
	private int space_info_no;
	
	public Select_UseVO() {
		super();
	}
	public Select_UseVO(int select_use_no, int space_uses_no, int space_info_no) {
		super();
		this.select_use_no = select_use_no;
		this.space_uses_no = space_uses_no;
		this.space_info_no = space_info_no;
	}
	public int getSelect_use_no() {
		return select_use_no;
	}
	public void setSelect_use_no(int select_use_no) {
		this.select_use_no = select_use_no;
	}
	public int getSpace_uses_no() {
		return space_uses_no;
	}
	public void setSpace_uses_no(int space_uses_no) {
		this.space_uses_no = space_uses_no;
	}
	public int getSpace_info_no() {
		return space_info_no;
	}
	public void setSpace_info_no(int space_info_no) {
		this.space_info_no = space_info_no;
	}

	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
