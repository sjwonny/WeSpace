package cat3.connection.vo;

public class Dsg_ClosedVO {
	private int dsg_closed_no;
	private String dsg_closed_day;
	private int usage_info_no;
	
	public Dsg_ClosedVO() {
		super();
	}
	public Dsg_ClosedVO(int dsg_closed_no, String dsg_closed_day, int usage_info_no) {
		super();
		this.dsg_closed_no = dsg_closed_no;
		this.dsg_closed_day = dsg_closed_day;
		this.usage_info_no = usage_info_no;
	}
	public int getDsg_closed_no() {
		return dsg_closed_no;
	}
	public void setDsg_closed_no(int dsg_closed_no) {
		this.dsg_closed_no = dsg_closed_no;
	}
	public String getDsg_closed_day() {
		return dsg_closed_day;
	}
	public void setDsg_closed_day(String dsg_closed_day) {
		this.dsg_closed_day = dsg_closed_day;
	}
	public int getUsage_info_no() {
		return usage_info_no;
	}
	public void setUsage_info_no(int usage_info_no) {
		this.usage_info_no = usage_info_no;
	}

	

	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
